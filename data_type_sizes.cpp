//This program determines the amount of memory used
#include <iostream>
using namespace std;

int main ()
{
	cout << "The size of a char variable is " << sizeof(char) << "bytes.\n";
	cout << "The size of a int variable is " << sizeof(int) << "bytes.\n";
	cout << "The size of a float variable is " << sizeof(float) << "bytes.\n";
	cout << "The size of a double variable is " << sizeof(double) << "bytes.\n";

	return 0;
}
	


	
