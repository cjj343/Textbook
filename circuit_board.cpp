//This program calulates the sales price of a product
#include <iostream>
using namespace std;

int main ()
{
	float profit, cost, total;
	profit = 1.40;
	cost = 12.67;
	total = profit*cost;

	cout << "The total cost of a product that cost $12.67 to make and returns a 40 percent profit is " << total << " dollars.\n";

	return 0;	
}
	


	
